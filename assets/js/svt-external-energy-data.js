/* SVT external energy data helpers.
   Reads the GitHub-cached OPCOM PZU JSON generated by scripts/fetch-opcom-pzu-cache.mjs.
*/
(function(root){
  const SOURCE_LABEL = "OPCOM PZU / ROPEX_DAM_15min";
  const RAW_LATEST_URL = "https://raw.githubusercontent.com/mrshoby/svt-energy-navigator/main/data/opcom/pzu/latest.json";
  const Q1_LOCAL_PROFILE = [0.74,0.71,0.69,0.70,0.74,0.82,0.96,1.12,1.08,0.98,0.90,0.84,0.82,0.84,0.90,1.02,1.17,1.34,1.43,1.33,1.15,0.98,0.86,0.77];
  const cache = { market:new Map(), bess:null };

  function todayYmd(){
    return new Date().toISOString().slice(0,10);
  }

  function cleanUrl(url){
    return String(url || "").trim();
  }

  function withCacheBuster(url){
    if (!/^https?:/i.test(url)) return url;
    const sep = url.includes("?") ? "&" : "?";
    return `${url}${sep}_svt=${Date.now()}`;
  }

  function labelForInterval(i){
    const h = Math.floor(i / 4);
    const m = (i % 4) * 15;
    return `${String(h).padStart(2,"0")}:${String(m).padStart(2,"0")}`;
  }

  function asNumber(value){
    if (value === null || value === undefined || value === "") return NaN;
    if (typeof value === "number") return Number.isFinite(value) ? value : NaN;
    let s = String(value).trim().replace(/\u00a0/g, " ").replace(/\s+/g, "");
    if (!s) return NaN;
    if (/^-?\d{1,3}(\.\d{3})+,\d+$/.test(s)) s = s.replace(/\./g, "").replace(",", ".");
    else if (/^-?\d{1,3}(,\d{3})+\.\d+$/.test(s)) s = s.replace(/,/g, "");
    else if (/^-?\d+,\d+$/.test(s)) s = s.replace(",", ".");
    const n = Number(s);
    return Number.isFinite(n) ? n : NaN;
  }

  function normalizeRecord(row, fallbackIndex){
    const intervalRaw = row && typeof row === "object" && row.interval != null ? Number(row.interval) : fallbackIndex + 1;
    const iRaw = row && typeof row === "object" && row.i != null ? Number(row.i) : intervalRaw - 1;
    const i = Math.max(0, Math.min(95, Number.isFinite(iRaw) ? Math.round(iRaw) : fallbackIndex));
    const rawPrice = typeof row === "number"
      ? row
      : asNumber(row?.priceLeiMwh ?? row?.priceLeiMWh ?? row?.price_lei_mwh ?? row?.rawPriceLeiMwh ?? row?.value ?? row?.price);
    if (!Number.isFinite(rawPrice) || rawPrice <= 0) return null;
    const priceLeiMwh = rawPrice < 10 ? rawPrice * 1000 : rawPrice;
    return {
      interval:i + 1,
      i,
      hour:i / 4,
      label:row?.label || labelForInterval(i),
      priceLeiMwh,
      priceRonKwh:priceLeiMwh / 1000,
      source:row?.source || SOURCE_LABEL
    };
  }

  function parseSeries(json){
    const arr = Array.isArray(json) ? json : (json?.records || json?.data || json?.intervals || json?.prices || []);
    if (!Array.isArray(arr) || !arr.length) return null;
    const byIndex = new Map();
    arr.forEach((row, idx) => {
      const rec = normalizeRecord(row, idx);
      if (rec) byIndex.set(rec.i, rec);
    });
    if (byIndex.size < 90) return null;
    const records = [];
    for (let i = 0; i < 96; i += 1) {
      const rec = byIndex.get(i);
      if (rec) records.push(rec);
    }
    return records.length >= 90 ? records : null;
  }

  function hourlyFromRecords(records){
    const hours = Array.from({length:24}, (_,hour)=>({hour, label:`${String(hour).padStart(2,"0")}:00`, priceLeiMwh:0, priceRonKwh:0, count:0}));
    (records || []).forEach(rec => {
      const hour = Math.max(0, Math.min(23, Math.floor(Number(rec.i || 0) / 4)));
      hours[hour].priceLeiMwh += Number(rec.priceLeiMwh || 0);
      hours[hour].priceRonKwh += Number(rec.priceRonKwh || 0);
      hours[hour].count += 1;
    });
    return hours.map(h => ({
      ...h,
      priceLeiMwh:h.count ? h.priceLeiMwh / h.count : 0,
      priceRonKwh:h.count ? h.priceRonKwh / h.count : 0
    }));
  }

  function marketAverageRonKwh(hourly){
    const vals = (hourly || []).map(h => Number(h.priceRonKwh || 0)).filter(v => v > 0);
    return vals.reduce((a,b)=>a+b,0) / Math.max(1, vals.length);
  }

  function scaledHourlyPrice(hour, fixedTariffRonKwh, marketData){
    const fixed = Number(fixedTariffRonKwh || 0.75) || 0.75;
    const index = Math.max(0, Math.min(23, Math.floor(Number(hour) || 0)));
    const live = marketData?.ok && marketData?.hourly ? marketData.hourly[index] : null;
    const liveAvg = marketAverageRonKwh(marketData?.hourly || []);
    if (live && liveAvg > 0 && Number(live.priceRonKwh) > 0) {
      return Math.max(0.01, fixed * Number(live.priceRonKwh) / liveAvg);
    }
    const avg = Q1_LOCAL_PROFILE.reduce((a,b)=>a+b,0) / Q1_LOCAL_PROFILE.length;
    return Math.max(0.01, fixed * Q1_LOCAL_PROFILE[index] / avg);
  }

  function buildCandidateUrls(day){
    const dayKey = /^\d{4}-\d{2}-\d{2}$/.test(String(day || "")) ? day : todayYmd();
    const configured = cleanUrl(root.SVT_OPCOM_CACHE_URL || root.SVT_EXTERNAL_OPCOM_CACHE_URL || "");
    const urls = [
      `./data/opcom/pzu/${dayKey}.json`,
      "./data/opcom/pzu/latest.json",
      configured,
      RAW_LATEST_URL
    ].filter(Boolean);
    return [...new Set(urls)];
  }

  async function fetchJson(url){
    const response = await fetch(withCacheBuster(url), {cache:"no-store", headers:{accept:"application/json,text/plain,*/*"}});
    if (!response.ok) throw new Error(`HTTP ${response.status}`);
    return response.json();
  }

  async function loadOpcomPzu(options={}){
    const day = options.day || options.deliveryDay || todayYmd();
    const key = String(day || "latest");
    if (cache.market.has(key)) return cache.market.get(key);
    const attempts = [];
    for (const url of buildCandidateUrls(day)) {
      try {
        const json = await fetchJson(url);
        if (json?.ok === false && !json.records && !json.data) throw new Error(json.error || "cache not ready");
        const records = parseSeries(json);
        if (!records) throw new Error("no usable PZU intervals");
        const result = {
          ok:true,
          provider:"opcom-pzu",
          sourceLabel:json.sourceLabel || json.source || SOURCE_LABEL,
          sourceMode:json.sourceMode || "external-cache-github",
          deliveryDay:json.deliveryDay || json.day || day,
          updatedAtUtc:json.updatedAtUtc || json.generatedAtUtc || json.fetchedAtUtc || null,
          sourceUrl:url,
          records,
          hourly:hourlyFromRecords(records)
        };
        cache.market.set(key, result);
        return result;
      } catch(error) {
        attempts.push(`${url}: ${error?.message || error}`);
      }
    }
    const fallback = {
      ok:false,
      provider:"fallback-local",
      sourceLabel:"profil local orientativ",
      sourceMode:"fallback-local",
      deliveryDay:day,
      error:attempts.join(" | "),
      records:null,
      hourly:null
    };
    cache.market.set(key, fallback);
    return fallback;
  }

  async function loadBessReference(){
    if (cache.bess) return cache.bess;
    try {
      const json = await fetchJson("./data/bess/inowattio_bess_reference_db.json");
      cache.bess = {ok:true, data:json, defaults:json.defaultSpecs || json.specs || null};
    } catch(error) {
      cache.bess = {ok:false, error:error?.message || String(error), data:null, defaults:null};
    }
    return cache.bess;
  }

  root.SVTExternalEnergyData = {
    parseSeries,
    hourlyFromRecords,
    loadOpcomPzu,
    loadBessReference,
    scaledHourlyPrice,
    marketAverageRonKwh
  };
})(typeof window !== "undefined" ? window : globalThis);
